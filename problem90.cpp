#include<iostream>
using namespace std;
int main()
{
    int t;
    cin>>t;
    for(int i=1;i<=t;i++)
    {
        int m, n;
        cin>>m>>n;
        int mat[m][n];
        for(int j=0;j<m;j++)
        {
            for(int k=0;k<n;k++)
                cin>>mat[j][k];
        }
        
    }
    return 0;
}